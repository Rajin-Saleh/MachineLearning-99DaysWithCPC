# 99DaysWithCPC - Machine Learning

name = input("What is your name? ")

print("Your name in title is", name.title())
print("Your name in lower is", name.lower())
print("Your name in upper is", name.upper())
print("Your name in capitalized is", name.capitalize())
print("Your name in casefold is", name.casefold())
print("Your name in isalnum is", name.isalnum())
